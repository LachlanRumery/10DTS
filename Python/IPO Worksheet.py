def PRODUCT_TWO_NUMS(n1,n2):
    return n1*n2

def SQUARE(n):
    return n*n

def AREA_RIGHT_ANGLED_TRIANGLE(base,height):
    return 0.5*base*height

def DAYS_TO_COVER(KMperDAY,KM):
    return KM/KMperDAY

def STUDENT_APPLE_BASKET(students,apples):
    return(apples % students,(apples-(apples % students))/students)
